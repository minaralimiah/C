#include <stdio.h>
#include <stdlib.h>
int main()
{   
    // Addition
    printf("%d + %d = %d\n", 5, 2, 5+2);
    // Subraction
    printf("%d - %d = %d\n", 5, 2, 5-2);
    // Multiplication
    printf("%d * %d = %d\n", 5, 2, 5*2);
    // Division
    printf("%d / %d = %f\n", 5, 2, 5/2);
    // Remainder
    printf("%d %% %d = %d\n", 5, 2, 5%2);
    return 0;
    
}